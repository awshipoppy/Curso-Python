# Tipos int e float
# int -> Número inteiro
# Número inteiro é um valor positivo ou negativo
# print (11) # int
# print (-11) # int
# print (0)

# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante
# print (1.1, 10.11)
# print (0.0, -1.5)

# A função type mostra o tipo que o Python interpretou
print (type('Otávio'))
print (type(1))
print (type(-1))
print (type(0))
print (type(1.1))




